async function run() {
  // 1. Get Toronto city_id
  const { data: city, error: cityError } = await supabase
    .from('cities')
    .select('id')
    .eq('slug', 'toronto')
    .single();

  if (cityError || !city) {
    console.error('❌ Could not fetch Toronto city ID', cityError);
    return;
  }

  const cityId = city.id;

  for (let batchIndex = 0; batchIndex < allBatches.length; batchIndex++) {
    const batch = allBatches[batchIndex];

    console.log(`🚀 Inserting batch ${batchIndex + 1} of ${allBatches.length}...`);

    for (const name of batch) {
      const formattedName = name.trim().replace(/\s+/g, ' ');
      const slug = slugify(formattedName, { lower: true });

      const { error } = await supabase
        .from('streets')
        .insert({ name: formattedName, slug, city_id: cityId });

      if (error && !error.message.includes('duplicate key')) {
        console.error(`❌ Failed: ${formattedName}`, error.message);
      } else {
        console.log(`✅ Inserted: ${formattedName}`);
      }

      await new Promise(res => setTimeout(res, 50)); // optional short delay (50ms)
    }
  }

  console.log('🎉 All batches completed.');
}

run();
